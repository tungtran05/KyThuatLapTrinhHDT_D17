package loptriangle;

import java.util.Scanner;


class Point {
    float x, y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
    
    public Point nextPoint(Scanner sc) {
        x = sc.nextFloat();
        y = sc.nextFloat();
        return this;
    }
}
